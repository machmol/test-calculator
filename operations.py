def plus(numOne, numTwo):
    return numOne + numTwo


def minus(numOne, numTwo):
    return int(numOne) - int(numTwo)


def multiple(numOne, numTwo):
    return int(numOne) * int(numTwo)


def division(numOne,numTwo):
    return int(numOne) / int(numTwo)


def modulo(numOne, numTwo):
    return int(numOne) % int(numTwo)